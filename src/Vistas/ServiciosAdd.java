    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String nombre = inputName.getText();
        String descrp = inputDescp.getText();
        int precio = (int)inputPrice.getValue();
        
        Servicio serv = new Servicio (nombre,descrp,precio);
        
        try {
            if (dao.guardarServicio(serv)) {
                JOptionPane.showMessageDialog(this, "Servicio Guardado");
            } else {
                JOptionPane.showMessageDialog(this, "Servicio No Guardado");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: "+ex.getMessage());
            Logger.getLogger(ServiciosAdd.class.getName()).log(Level.SEVERE, null, ex);
        }
                
        JOptionPane.showMessageDialog(this, "Empleado Registrado");
        Limpiar();
        
    }//GEN-LAST:event_btnAddActionPerformed

